// ARITHMETIC OPERATORS

let x;
x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 7 % 5;

console.log(x);

// CONCATENATION

let y = "Hello" + " " + "World";

console.log(y);

// EXPONENT

let b = 2 ** 3;
console.log(b);

// INCREMENT
let a = 1;
a++; // a = a + 1
console.log(a);
// DECREMENT
a--;
// a = a -1

//Assignment Operators

let q = 10;
q += 5; // q = q + 5
// similarly -=, *=, /=, %=, **=

console.log(q);

// Comparison Operators
let r = 2;
r = 2 == 2; // == only checks if value is same doesn't check if type is same
r = 2 === "2"; // ===, it also checks if data typpe is same

r = 2 != 2;
r = 2 !== "2";

r = 10 > 5; // <, <=, >=
console.log(r);
